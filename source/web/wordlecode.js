function checkWord(word){
  // given a word, checks if it is in the wordle list
  var word_list=getWords().map(function(r){ return atob(r)});
  for(let i=0;i<word_list.length;i++)
  {
    if (word===word_list[i])
      return true
  }
  return false;

}
function getPossibleWords(wordList,clue,clueword)
{
  /* function to return a list of possible words
     given the clue and clueword and list of word
    clue and clueword can be arrays
  */


  var sol_list=[]
  var result=true
  for(let i=0;i<wordList.length;i++){
    result=true
    for(let j=0;j<clue.length;j++){
      result=(true && this.checkWord(wordList[i],clue[j],clueword[j]))
      if(result==false){
        break
      }
    }
    if(result==true){
      sol_list.push(wordList[i])
    }
  }
  return sol_list

}







